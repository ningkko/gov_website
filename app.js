// 手册渲染（从 handbook.txt 加载）
// ============================================
function renderHandbook() {
    const pageHtml = `
        <div class="page">
            <div class="nav-bar">
                <div class="nav-bar-back" onclick="window.location.hash = '#/home'">←</div>
                <div class="nav-bar-title">政策问答手册</div>
                <div class="nav-bar-spacer"></div>
            </div>

            <div class="collection-detail-container">
                <div class="collection-detail-warning">
                    ⚠️ 温馨提示：以下为手册内容示例，后续请以正式文件为准。
                </div>

                <div id="handbook-content" class="collection-detail-content">正在加载手册内容……</div>

                <div class="action-buttons">
                    <a href="#/home" class="btn btn-secondary">返回首页</a>
                </div>
            </div>
        </div>
    `;

    app.innerHTML = pageHtml;

    // 尝试加载 handbook.txt（已复制到项目根）
    fetch('/handbook.txt')
        .then(resp => {
            if (!resp.ok) throw new Error('无法加载手册文本');
            return resp.text();
        })
        .then(text => {
            const container = document.getElementById('handbook-content');
            if (!container) return;
            // 按空行分段并转为 <p>
            const paragraphs = text.split(/\n\s*\n/)
                .map(p => `<p>${escapeHtml(p.trim())}</p>`)
                .join('');
            container.innerHTML = paragraphs;
        })
        .catch(err => {
            const container = document.getElementById('handbook-content');
            if (container) container.textContent = '手册加载失败：' + err.message;
        });
}

