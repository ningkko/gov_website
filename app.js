function renderQAList(categoryId) {
    const category = qaCategories.find(c => c.id === categoryId);
    if (!category) {
        renderHome();
        return;
    }

    const items = qaItems.filter(q => q.categoryId === categoryId);

    // 提取编号：从ID中提取数字，如 'qa-employment-1' -> '1'
    let itemsHtml = items.map((item, index) => {
        const numberMatch = item.id.match(/(\d+)$/);
        const number = numberMatch ? numberMatch[1] : (index + 1);
        return `
        <a href="#/qa-detail/${item.id}" class="card">
            <div class="card-question"><span class="question-number">${number}.</span> ${escapeHtml(item.question)}</div>
        </a>
    `;
    }).join('');

    const pageHtml = `
        <div class="page">
            <div class="nav-bar">
                <div class="nav-bar-back" onclick="window.location.hash = '#/home'">←</div>
                <div class="nav-bar-title">${escapeHtml(category.name)}</div>
                <div class="nav-bar-spacer"></div>
            </div>

            <div class="search-bar">
                <input type="text" id="search-input" placeholder="搜索问答...">
                <button onclick="filterQA()">搜索</button>
            </div>

            <div class="card-list" id="qa-list">
                ${itemsHtml}
            </div>
        </div>
    `;

    app.innerHTML = pageHtml;

    // 添加搜索功能
    window.filterQA = function() {
        const searchKeyword = document.getElementById('search-input').value.toLowerCase();
        const qaList = document.getElementById('qa-list');
        const filteredItems = items.filter(item => 
            item.question.toLowerCase().includes(searchKeyword) ||
            item.answer.toLowerCase().includes(searchKeyword)
        );
        qaList.innerHTML = filteredItems
            .map((item, index) => {
                const numberMatch = item.id.match(/(\d+)$/);
                const number = numberMatch ? numberMatch[1] : (index + 1);
                return `
                <a href="#/qa-detail/${item.id}" class="card">
                    <div class="card-question"><span class="question-number">${number}.</span> ${escapeHtml(item.question)}</div>
                </a>
            `;
            }).join('');
    };

    // 支持回车搜索
    document.getElementById('search-input').addEventListener('keypress', function(e) {
        if (e.key === 'Enter') {
            window.filterQA();
        }
    });
}

